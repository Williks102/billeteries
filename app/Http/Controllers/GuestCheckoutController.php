    /**
     * G√©rer la cr√©ation/r√©cup√©ration de l'utilisateur (VOTRE LOGIQUE)
     */
    private function handleUser(Request $request)
    {
        Log::info('D√©but handle user invit√©', [
            'email' => $request->email,
            'create_account' => $request->boolean('create_account')
        ]);
        
        $email = $request->email;
        $existingUser = User::where('email', $email)->first();
        $createAccount = $request->boolean('create_account');
        
        // Si utilisateur existe
        if ($existingUser) {
            Log::info('Utilisateur existant trouv√©', [
                'user_id' => $existingUser->id,
                'is_guest' => $existingUser->is_guest
            ]);
            
            // Si demande cr√©ation de compte mais utilisateur normal existe
            if ($createAccount && !$existingUser->is_guest) {
                throw new \Exception('Un compte existe d√©j√† avec cet email. Veuillez vous connecter.');
            }
            
            // Si utilisateur normal, l'utiliser
            if (!$existingUser->is_guest) {
                return $existingUser;
            }
            
            // Si cr√©ation de compte sur un invit√© existant
            if ($createAccount && $existingUser->is_guest) {
                Log::info('Conversion invit√© vers compte');
                $existingUser->update([
                    'name' => $request->first_name . ' ' . $request->last_name,
                    'first_name' => $request->first_name,
                    'last_name' => $request->last_name,
                    'phone' => $request->phone,
                    'password' => Hash::make($request->password),
                    'is_guest' => false,
                    'guest_converted_at' => now()
                ]);
                auth()->login($existingUser);
                return $existingUser;
            }
            
            // Mise √† jour invit√© existant
            Log::info('Mise √† jour invit√© existant');
            $existingUser->update([
                'name' => $request->first_name . ' ' . $request->last_name,
                'first_name' => $request->first_name,
                'last_name' => $request->last_name,
                'phone' => $request->phone,
            ]);
            return $existingUser;
        }
        
        // Utilisateur n'existe pas - cr√©er selon le mode
        if ($createAccount) {
            Log::info('Cr√©ation nouveau compte');
            $user = User::create([
                'name' => $request->first_name . ' ' . $request->last_name,
                'first_name' => $request->first_name,
                'last_name' => $request->last_name,
                'email' => $email,
                'phone' => $request->phone,
                'password' => Hash::make($request->password),
                'role' => 'acheteur',
                'email_verified_at' => now()
            ]);
            auth()->login($user);
            return $user;
        } else {
            Log::info('Cr√©ation nouvel invit√©');
            return User::create([
                'name' => $request->first_name . ' ' . $request->last_name,
                'first_name' => $request->first_name,
                'last_name' => $request->last_name,
                'email' => $email,
                'phone' => $request->phone,
                'password' => Hash::make(Str::random(16)),
                'role' => 'acheteur',
                'is_guest' => true
            ]);
        }
    }

    /**
     * Page de confirmation pour invit√©s (m√©thode existante)
     */
    public function confirmation($token)
    {
        $order = Order::where('guest_token', $token)
                     ->with(['user', 'event', 'orderItems.ticketType', 'tickets'])
                     ->firstOrFail();
        
        return view('checkout.guest-confirmation', compact('order'));
    }

    /**
     * Permettre √† un invit√© de cr√©er un compte apr√®s achat (m√©thode existante)
     */
    public function createAccountAfterPurchase(Request $request, $token)
    {
        // Votre m√©thode existante reste identique
        $request->validate([
            'password' => 'required|min:6|confirmed'
        ]);

        $order = Order::where('guest_token', $token)->firstOrFail();
        $user = $order->user;

        if (!$user->is_guest) {
            return redirect()->route('home')->with('error', 'Action non autoris√©e.');
        }

        try {
            DB::beginTransaction();

            // Convertir l'invit√© en utilisateur normal
            $user->update([
                'name' => $user->first_name . ' ' . $user->last_name,
                'password' => Hash::make($request->password),
                'is_guest' => false,
                'guest_converted_at' => now()
            ]);

            // Supprimer les tokens invit√©
            Order::where('user_id', $user->id)->update(['guest_token' => null]);

            // Connecter l'utilisateur
            auth()->login($user);

            DB::commit();

            return redirect()->route('acheteur.dashboard')->with('success', 
                'Votre compte a √©t√© cr√©√© ! Vous pouvez maintenant g√©rer toutes vos commandes.');

        } catch (\Exception $e) {
            DB::rollBack();
            return back()->withErrors(['error' => 'Erreur lors de la cr√©ation du compte.']);
        }
    }
